// Boolean
var isTrue = true;
var isFalse = false;
console.log(isTrue);
console.log(isFalse);
// Any
var anyValue = 5;
console.log(anyValue);
anyValue = 'Hello';
console.log(anyValue);
// Array of Strings
var cars = ['Mercedes', 'BMW', 'Bugatti'];
console.log(cars);
// Heterogeneous Array
var mixedArray = ['Bugatti', 5, true];
console.log(mixedArray);
